  public static void run()
  {
    int a, b, offset, dy;

    SegaGenesis.setPaletteColors(palette);
    SegaGenesis.setPatternTable(pattern);
    SegaGenesis.setImageData(image);

    offset = 0;
    dy = 1;

    while(true)
    {
      SegaGenesis.setPalettePointer(0);
      SegaGenesis.setPaletteColor(0);

      while(SegaGenesis.inVerticalBlank());

      for (b = 0; b < offset; b++)
      {
        SegaGenesis.waitHorizontalBlank();
      }

      for (a = 0; a < 0xf; a++)
      {
        SegaGenesis.setPalettePointer(0);
        SegaGenesis.setPaletteColor((a << 8) | (a << 4) | a);
        SegaGenesis.waitHorizontalBlank();
      }

      SegaGenesis.setPalettePointer(0);
      SegaGenesis.setPaletteColor(0);

      SegaGenesis.waitVerticalBlank();

      offset += dy;
      if (offset == 223 - 16) { dy = -1; }
      if (offset == 0) { dy = 1; }
    }
  }

