  public static void fadeIn()
  {
    int i;
    short[] colors = Memory.allocStackShorts(12);
    short color;
    int count;

    for (i = 0; i < 12; i++)
    {
      colors[i] = 0;
    }

    for (count = 0; count < 8; count++)
    {
      SegaGenesis.setPalettePointer(0);

      for (i = 0; i < 12; i++)
      {
        color = colors[i];

        if ((color & 0x00f) < (palette[i] & 0x00f)) { color += 0x002; }
        if ((color & 0x0f0) < (palette[i] & 0x0f0)) { color += 0x020; }
        if ((color & 0xf00) < (palette[i] & 0xf00)) { color += 0x200; }

        colors[i] = color;

        SegaGenesis.setPaletteColor(color);
        Common.wait(1);
      }
    }
  }

  public static void shiftFull()
  {
    int dx = 3;
    int x = 0;
    int i;

    SegaGenesis.setHorizontalScrollModeFull();

    for (i = 0; i < 32 * 5; i++)
    {
      SegaGenesis.setHorizontalScrollB(0, x);

      x += dx;

      if (x <= -32) { dx = 3; }
      if (x >= 32) { dx = -3; }

      Common.wait(5);
    }

    SegaGenesis.setHorizontalScrollModeLine();
    SegaGenesis.setHorizontalScrollB(0, 0);
  }

  public static void run()
  {
    int i;

    SegaGenesis.setPalettePointer(0);

    for (i = 0; i < 15; i++)
    {
      SegaGenesis.setPaletteColor(0x000);
    }

    SegaGenesis.setPatternTable(pattern);
    SegaGenesis.setImageData(image);
    fadeIn();
    SegaGenesis.setPaletteColors(ImageInside.palette);
    Common.wait(120);

    shiftFull();
  }

